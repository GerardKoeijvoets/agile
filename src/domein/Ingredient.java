package domein;

public class Ingredient extends Recipe {

	String Name;
	String Type;

	public Ingredient() {
	}

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}

	public String getType() {
		return Type;
	}

	public void setType(String type) {
		Type = type;
	}

}
